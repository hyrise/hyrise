import numpy as np
import seaborn as sns

parser = ArgumentParser(
    description="This script plots the results of a radix partitioning micro benchmark. First run the micro benchmark."
    "Then strip the first few lines from the output, such that the file is proper csv. Make sure to keep the csv"
    "header. After that, you can run this script.",
    formatter_class=ArgumentDefaultsHelpFormatter,
)
parser.add_argument("-f", "--file", type=str, help="The input csv file that was generated by the micro benchmark")
args = parser.parse_args()


def convert(df):
    df[["name", "radix_bits", "time1", "time2"]] = df.name.str.split("/", expand=True)
    df[["Variant", "locality"]] = (
        df["name"].map(lambda name: name.split("<")[1].split(">")[0]).str.split(", ", expand=True)
    )
    df["radix_bits"] = pd.to_numeric(df["radix_bits"])
    return df.pivot_table(values="real_time", index="radix_bits", columns=("Variant")) / 1_000_000


df = pd.read_csv(args.file)
df_std = convert(df[df.name.str.contains("std")])
df = convert(df[df.name.str.contains("median")])

df_median_long = df.reset_index().melt(id_vars='radix_bits', var_name='Variant', value_name='median_runtime')
df_std_long  = df_std.reset_index().melt(id_vars='radix_bits', var_name='Variant', value_name='std')
df_long = df_median_long.merge(df_std_long, on=['radix_bits', 'Variant'])
df_long["row_count"] = (df_long.radix_bits**2) * 56 * 1_000
df_long["normalized_runtime"] = (df_long.median_runtime / df_long.row_count) * 1_000_000

