from argparse import ArgumentParser, ArgumentDefaultsHelpFormatter
from os.path import isdir

import matplotlib.pyplot as plt
import pandas as pd
import numpy as np

parser = ArgumentParser(
    description="This script plots the results of a radix partitioning micro benchmark. First run the micro benchmark."
    "Then strip the first few lines from the output, such that the file is proper csv. Make sure to keep the csv"
    "header. After that, you can run this script.",
    formatter_class=ArgumentDefaultsHelpFormatter,
)
parser.add_argument("-f", "--file", type=str, help="The input csv file that was generated by the micro benchmark")
parser.add_argument(
    "-o",
    "--output",
    type=str,
    default=".",
    help="The output location. If this is an existing folder, the output will be a file with the same name as the input"
    "in the output folder. Otherwise, it will be a file at this exact path.",
)
args = parser.parse_args()


def convert(df):
    df[["name", "items", "radix bits"]] = df.name.str.split("/", expand=True)
    df[["Variant", "locality"]] = (
        df["name"].map(lambda name: name.split("<")[1].split(">")[0]).str.split(", ", expand=True)
    )
    # df = df[df["locality"] != 1]
    df["items"] = pd.to_numeric(df["items"])
    df["Radix Bits"] = pd.to_numeric(df["radix bits"].map(lambda bits: bits.split("_")[0]))
    return df.pivot_table(values="cpu_time", index="Radix Bits", columns=("items", "Variant")) / 1_000_000


df = pd.read_csv(args.file)
df_std = convert(df[df.name.str.contains("std")])
df = convert(df[df.name.str.contains("median")])

fig, axs = plt.subplots(ncols=5, nrows=2, figsize=(20, 10), layout="constrained")
for (i, row), ax in zip(df.iterrows(), (ax for axr in axs for ax in axr)):
    row.unstack().plot(
        kind="bar",
        ax=ax,
        ylabel="Cpu Time (ms)",
        xlabel="Items",
        title=f"{i} Radix Bit" + "s" if i > 1 else "",
        legend=(i == 1),
        yerr=df_std.loc[i].unstack(),
    )

fig.suptitle("Median runtime of partition_by_radix over at least 100 runs (and one warmup run) on Nemea")
axs[0][0].legend()
if isdir(args.output):
    fig.savefig(f"{args.output}/{args.file}.svg")
else:
    fig.savefig(f"{args.output}")

